package E5String类.C3String方法;

public class length方法9 {
    public static void main(String[] args) {

        //(掌握)int length()
        //面试题：判断数组长度和判断字符串长度不一样
        //判断数组长度是length属性，判断字符串长度是length()方法
        System.out.println("abc".length());  //3
        System.out.println("".length());     //0
    }
}
