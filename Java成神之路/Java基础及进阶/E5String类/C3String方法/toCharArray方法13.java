package E5String类.C3String方法;

public class toCharArray方法13 {
    public static void main(String[] args) {

        //(掌握) char[] toCharArray()
        //将字符串转换成char数组
        char[] chars ="我是中国人".toCharArray();
        for (int i = 0; i < chars.length; i++) {
            System.out.println(chars[i]);
        }
    }
}
