package E5String类.C3String方法;

public class indexOf方法7 {
    public static void main(String[] args) {

        //(掌握)int indexOf(String str)
        //判断某个字符串在当前字符串中第一次出现处的索引
        System.out.println("jbd$$skfjavahds##fas%%djava++".indexOf("java"));

        //(掌握) int lastIndexOf()
        //判断某个字符串在当前字符串中最后一次出现处的索引
        System.out.println("jbd$$skfjavahds##fas%%djava++".lastIndexOf("java"));
    }
}
